enum {
  LCD_PRINT_INT_MAX_WIDTH = 11
};
uint8_t LCD_Print_Buffer[LCD_PRINT_INT_MAX_WIDTH + 2];

void LCD_Print_Int(int32_t value) {
  uint8_t pos = LCD_PRINT_INT_MAX_WIDTH;
  uint8_t sign = 0;
  if (value < 0) {
    sign = 1;
    value = -value;
  }

  LCD_Print_Buffer[LCD_PRINT_INT_MAX_WIDTH + 1] = 0;

  do {
    LCD_Print_Buffer[pos--] = value % 10 + '0';
    value /= 10;
  } while (value != 0);

  if (sign) {
    LCD_Print_Buffer[pos--] = '-';
  }

  LCD_Print_String(LCD_Print_Buffer + pos + 1);
}

void LCD_Print_String(const uint8_t *s) {
  for(; *s != 0; s++) {
    LCD_Print_Char(*s);
  }
}

void LCD_Print_Char(uint8_t c) {
  const uint8_t *cb;
  uint8_t l = 0, r = 4;

  if (c > '~') {
    c -= 65;
  }
  c -= 32;

  cb = lcd_chars[c];

  if (c != 0) {
    while (cb[l] == 0) {
      l++;
    }
    while (cb[r] == 0) {
      r--;
    }
  } else {
    r = 0;
  }
  LCD_Write(LCD_DATA, cb + l, r - l + 2, 1);
}

void LCD_Config() {
  GPIO_InitTypeDef GPIO_init_str;
  SPI_InitTypeDef SPI_init_str;

  /* Configure GPIO for CS/RST pins */
  RCC_AHBPeriphClockCmd(LCD_GPIO_RCC, ENABLE);
  GPIO_init_str.GPIO_Pin   = LCD_RES_PIN | LCD_CS_PIN;
  GPIO_init_str.GPIO_Mode  = GPIO_Mode_OUT;
  GPIO_init_str.GPIO_Speed = GPIO_Speed_50MHz;
  GPIO_Init(LCD_GPIO, &GPIO_init_str);
  LCD_RES_HIGH;
  LCD_CS_LOW; // Select chip?

  /* Enable hardware SPI */
  RCC_APB2PeriphClockCmd(LCD_SPI_RCC, ENABLE);

  /* Configure GPIO for SDA/CLK pins */
  RCC_AHBPeriphClockCmd(LCD_SPI_GPIO_RCC, ENABLE);
  GPIO_PinAFConfig(LCD_SPI_GPIO, LCD_SDA_PIN_SOURCE, GPIO_AF_0);
  GPIO_PinAFConfig(LCD_SPI_GPIO, LCD_SCK_PIN_SOURCE, GPIO_AF_0);

  /* Configure GPIO pins for SPI */
  GPIO_init_str.GPIO_Pin = LCD_SDA_PIN | LCD_SCK_PIN;
  GPIO_init_str.GPIO_Mode = GPIO_Mode_AF;
  GPIO_init_str.GPIO_Speed = GPIO_Speed_50MHz;
  GPIO_init_str.GPIO_OType = GPIO_OType_PP;
  GPIO_init_str.GPIO_PuPd = GPIO_PuPd_NOPULL;
  GPIO_Init(LCD_SPI_GPIO, &GPIO_init_str);
  SPI_StructInit(&SPI_init_str);

  SPI_init_str.SPI_Direction = SPI_Direction_1Line_Tx;  // Set direction - TX only
  SPI_init_str.SPI_Mode = SPI_Mode_Master;  //Set chip as master
  SPI_init_str.SPI_DataSize = SPI_DataSize_9b; //Set word width to 9 bit (include data/command bit)
  SPI_init_str.SPI_CPOL = SPI_CPOL_High; //Assume clock high in IDLE
  SPI_init_str.SPI_CPHA = SPI_CPHA_2Edge; //Set data set at upper edge of a clock
  SPI_init_str.SPI_NSS = SPI_NSS_Soft; //Disable hardware Chip Select
  SPI_init_str.SPI_BaudRatePrescaler = SPI_BaudRatePrescaler_4; //Set SPI bitrate
  SPI_init_str.SPI_FirstBit = SPI_FirstBit_MSB; //Transmit most significant byte first
  SPI_Init(LCD_SPI, &SPI_init_str);

  SPI_NSSInternalSoftwareConfig(LCD_SPI, SPI_NSSInternalSoft_Set); //Set internal value of Chip Select to high
  SPI_Cmd(LCD_SPI, ENABLE);

  /* Reset LCD */
  LCD_RES_LOW;
  Delay(5);
  LCD_RES_HIGH;
  Delay(5);

  LCD_Write(LCD_COMMAND, lcd_reset_sequence, sizeof(lcd_reset_sequence), 1); //Send internal reset
  Delay(20); //Wait while reset finish
  LCD_Write(LCD_COMMAND, lcd_init_sequence, sizeof(lcd_init_sequence), 1); //Send initialization sequence
  LCD_Clear();
}

void LCD_Write(uint8_t cd, const uint8_t * data, uint16_t len, uint16_t repeat)
{
  uint8_t i;
  uint16_t octet;

  while(repeat > 0) {
    for(i = 0; i < len; i++) {
      octet = data[i];
      if (cd) {
        octet |= 0x100;
      }
      /* Wait until SPI is busy */
      while (SPI_I2S_GetFlagStatus(LCD_SPI, SPI_I2S_FLAG_TXE) == RESET);
      /* Transmit data */
      LCD_SPI->DR = octet;
    }
    repeat --;
  }
}

void LCD_Clear()
{
  LCD_Write(1, &LCD_ZERO, 1, 864);
}

void LCD_GotoXY(uint8_t x, uint8_t y)
{
  uint8_t sequence[3];
  sequence[0] = 0xB0 | (y & 0x0F);        //Y addressing: 1011yyyy
  sequence[1] = x & 0x0F;                 //X addressing: 0000xxxx, lower bits
  sequence[2] = 0x10 | ((x >> 4) & 0x07); //X addressing: 00010xxx, higher bits
  LCD_Write(LCD_COMMAND, sequence, 3, 1);
  LCD_CurrentX = x;
  LCD_CurrentY = y;
}
